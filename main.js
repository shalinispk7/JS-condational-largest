let b= parseInt(prompt("Enter value b"));
let c= parseInt(prompt("Enter value c"));
if (a>b && a>c){
document.write("Value A is greater");
}
else if (b>a && b>c){
document.write("Value B is greater");
}
else if (c>a && c>b){
document.write("Value C is greater");
}